// Send a command byte
static void sh1107_cmd(uint8_t cmd) {
  uint8_t buf[2] = {0x00, cmd};
  nrf_twi_mngr_transfer_t const transfer[] = {
    NRF_TWI_MNGR_WRITE(SH1107_ADDRESS, buf, 2, 0),
  };
  nrf_twi_mngr_perform(i2c_manager, NULL, transfer, 1, NULL);
}

void ssd1306_init(const nrf_twi_mngr_t* i2c) {
  i2c_manager = i2c;
  nrf_delay_ms(100);

  sh1107_cmd(0xAE); // Display OFF

  sh1107_cmd(0xD5); // Set clock divide ratio
  sh1107_cmd(0x80);

  sh1107_cmd(0xA8); // Set multiplex ratio
  sh1107_cmd(0x3F); // 64 lines

  sh1107_cmd(0xD3); // Set display offset
  sh1107_cmd(0x00);

  sh1107_cmd(0x40); // Set start line 0

  sh1107_cmd(0x8D); // Charge pump
  sh1107_cmd(0x14); // Enable

  sh1107_cmd(0x20); // Memory addressing mode
  sh1107_cmd(0x00); // Horizontal

  sh1107_cmd(0xA1); // Segment re-map
  sh1107_cmd(0xC8); // COM scan direction

  sh1107_cmd(0xDA); // COM pins config
  sh1107_cmd(0x12); // Alternative COM pin, for 128x64

  sh1107_cmd(0x81); // Set contrast
  sh1107_cmd(0xCF);

  sh1107_cmd(0xD9); // Set pre-charge period
  sh1107_cmd(0xF1);

  sh1107_cmd(0xDB); // Set VCOMH deselect level
  sh1107_cmd(0x40);

  sh1107_cmd(0xA4); // Display from RAM
  sh1107_cmd(0xA6); // Normal display
  sh1107_cmd(0x2E); // Deactivate scroll

  sh1107_cmd(0xAF); // Display ON

  nrf_delay_ms(100);

  ssd1306_clear();
  ssd1306_display();
  printf("OLED initialized (0x3D)\n");
}

void ssd1306_clear(void) {
  memset(display_buf, 0, sizeof(display_buf));
  cursor_col = 0;
  cursor_page = 0;
}

void ssd1306_set_cursor(uint8_t col, uint8_t page) {
  cursor_col = col;
  cursor_page = page;
}

static void ssd1306_write_char(char c) {
  if (c < 32 || c > 126) return;
  if (cursor_col + 5 >= OLED_WIDTH) return;

  uint8_t idx = c - 32;
  uint16_t buf_offset = cursor_page * OLED_WIDTH + cursor_col;

  for (int i = 0; i < 5; i++) {
    display_buf[buf_offset + i] = font_5x7[idx][i];
  }
  display_buf[buf_offset + 5] = 0x00;
  cursor_col += 6;
}

void ssd1306_write_string(const char* str) {
  while (*str) {
    ssd1306_write_char(*str++);
  }
}

void ssd1306_draw_pixel(uint8_t x, uint8_t y, bool on) {
  if (x >= OLED_WIDTH || y >= OLED_HEIGHT) return;
  uint8_t page = y / 8;
  uint8_t bit = y % 8;
  uint16_t offset = page * OLED_WIDTH + x;
  if (on) {
    display_buf[offset] |= (1 << bit);
  } else {
    display_buf[offset] &= ~(1 << bit);
  }
}

void ssd1306_draw_hline(uint8_t x, uint8_t y, uint8_t width) {
  for (uint8_t i = 0; i < width && (x + i) < OLED_WIDTH; i++) {
    ssd1306_draw_pixel(x + i, y, true);
  }
}

void ssd1306_draw_bitmap(uint8_t x, uint8_t page, const uint8_t* bitmap, uint8_t w, uint8_t pages) {
  for (uint8_t p = 0; p < pages; p++) {
    if (page + p >= OLED_PAGES) break;
    uint16_t buf_offset = (page + p) * OLED_WIDTH + x;
    for (uint8_t col = 0; col < w && (x + col) < OLED_WIDTH; col++) {
      display_buf[buf_offset + col] |= bitmap[p * w + col];
    }
  }
}

void ssd1306_write_string_2x(const char* str) {
  while (*str) {
    char c = *str++;
    if (c < 32 || c > 126) continue;
    if (cursor_col + 11 >= OLED_WIDTH) break;

    uint8_t idx = c - 32;
    uint16_t top_offset = cursor_page * OLED_WIDTH + cursor_col;
    uint16_t bot_offset = (cursor_page + 1) * OLED_WIDTH + cursor_col;

    for (int i = 0; i < 5; i++) {
      uint8_t glyph = font_5x7[idx][i];
      // Stretch each column vertically: each bit becomes 2 bits
      uint16_t stretched = 0;
      for (int b = 0; b < 7; b++) {
        if (glyph & (1 << b)) {
          stretched |= (3 << (b * 2));
        }
      }
      uint8_t top_byte = stretched & 0xFF;
      uint8_t bot_byte = (stretched >> 8) & 0xFF;
      // Double each column horizontally
      if (cursor_page + 1 < OLED_PAGES) {
        display_buf[top_offset + i * 2] = top_byte;
        display_buf[top_offset + i * 2 + 1] = top_byte;
        display_buf[bot_offset + i * 2] = bot_byte;
        display_buf[bot_offset + i * 2 + 1] = bot_byte;
      }
    }
    // 2px gap between chars
    display_buf[top_offset + 10] = 0;
    display_buf[top_offset + 11] = 0;
    if (cursor_page + 1 < OLED_PAGES) {
      display_buf[bot_offset + 10] = 0;
      display_buf[bot_offset + 11] = 0;
    }
    cursor_col += 12;
  }
}

void ssd1306_display(void) {
  // Set column address range: 0-127
  sh1107_cmd(0x21);
  sh1107_cmd(0x00);
  sh1107_cmd(0x7F);
  // Set page address range: 0-7
  sh1107_cmd(0x22);
  sh1107_cmd(0x00);
  sh1107_cmd(OLED_PAGES - 1);

  // Send display buffer in chunks
  for (int i = 0; i < OLED_PAGES * OLED_WIDTH; i += 32) {
    uint8_t chunk[33];
    chunk[0] = 0x40; // Data mode
    int len = 32;
    if (i + len > OLED_PAGES * OLED_WIDTH) {
      len = OLED_PAGES * OLED_WIDTH - i;
    }
    memcpy(&chunk[1], &display_buf[i], len);

    nrf_twi_mngr_transfer_t const transfer[] = {
      NRF_TWI_MNGR_WRITE(SH1107_ADDRESS, chunk, len + 1, 0),
    };
    nrf_twi_mngr_perform(i2c_manager, NULL, transfer, 1, NULL);
  }
}
